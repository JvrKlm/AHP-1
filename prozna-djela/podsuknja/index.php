      <p>(...) Kakav je roman napisala Ana Horvat?(...) Ona ispisuje tekst čija se svaka rečenica nastoji legitimirati kao fikcijski fragment jedne izmišljene superstrukture, dajući s druge strane dovoljno signala autobiografskoj inspiraciji kao daleku izvanfikcijskom jamstvu fikcijskog svijeta, pretvarajući tako, što i nije neviđeni postupak, osobno iskustvo u literarni zapis o ljudskome iskustvu. (...)</p>
      <p>(...) Ipak, autorica nipošto nije ispisala staromodni roman. Težnja da ocrta suvremen senzibilitet, neoprezan u svojoj brzini, nesmiljen u potrazi za novim senzacijama, površan u nespravnosti da se suoči s dramom života, možda je i prenaglašen u njenu spisateljskom postupku. S druge strane, jednako je naglašen i pokušaj da svemu neautentičnome čime nas je obdario postmoderni duh suprotstavi jednostavnost glavne junakinje, osjećajne i željne načela izočnih u suvremenu svijetu. Zanimljiv je to pokušaj. Ne radi se ovdje o nekoj patetici ili anakronizmu. (...)</p>
      <p>(...)Urbana pojava, dijete zagrebačkog asfalta, s tipičnim refleksima velegradskog čeda, lišena sentimentalnosti, još više iluzija, cinična do mjere u kojoj cinizam ne truje nego spašava dušu od gorčine i gađenja nad svijetom koji je u svojoj bezličnosti i okrutnosti, postao naša sudbina.</p>
