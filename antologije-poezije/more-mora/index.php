			<h2>
				Kolo 4, 2014.<br /> Kritika<br /> Lada Žigo<br /> More u svojoj
				sveznačnosti i neukrotivosti<br /> <small>(Ana Horvat: More mora
					(hrvatski pjesnici o moru), Naklada Đuretić, Zagreb, 2014.)</small>
			</h2>

			<p>More (voda), ta vječna, neiscrpna tema, uvijek je izbjegavalo
				definicijama i davalo bezgraničnu slobodu filozofima, pjesnicima,
				prozaicima i pustolovima da zaranjaju u njegova značenja i tajne.
				Prostor vode je golem, a njezino je vrijeme bezgranično – stoga je
				već u prvoj filozofskoj školi starih Grka (Miletskoj školi) voda
				dobila i metafizički status (Tales je smatra prapočelom sviju
				stvari, sve je poteklo od vode, što dokazuje vlažnost svih sjemenki
				na zemlji). I Empedoklo je smatra jednim od praelemenata (uz zemlju,
				vatru, zrak) koji u zajedničkom vrtlogu stvaraju neslogu (Mržnju), a
				u zajedničkom skladu kozmičku ravnotežu (Ljubav). Osim filozofskog
				poimanja vode, postoji i dubinsko doživljavanje vode, preplitanje
				vodenih mijena s mijenama duše, istraživanje esencije vode, odnosno
				poistovjećivanje nepredvidivosti vode sa samom biti ljudske
				egzistencije.</p>
			<p>Prvi je Ivan Gundulić opjevao more kao metaforu života: »Ah, nije
				život ljucki drugo / neg smućeno jedno more, / neg plav jedna, ku
				udugo / biju vali kako gore; / i sred ovijeh netom tmina / čo’ek se
				rodi, mrijet počina.« I mnogi će pjesnici poslije njega samoživost
				mora uspoređivati s mnogobrojnim stanjima ljudske duše, od oluje do
				bonace, od silovitosti do pomirljivosti, pa će motrenje mora (ili
				stapanje bića s morem na pučini) u čitavoj povijesti hrvatske
				književnosti ostati jednim od najučestalijih pjesničkih i duševnih
				stanja, »vječnih« tema koje potiru svaku podjelu na »staromodno« i
				»novo«. Kod brojnih pjesnika u ovoj antologiji prisutno je more,
				prije svega, kao simbol neuhvatljiva i prevrtljiva života, pa tako,
				npr. Enerika Bijač pjeva: »Ovo more što preda mnom blista u bezbroj
				/ izlomljenih ploha, u nemir koji oživljava glatku / površinu –
				ukriženi je govor svjetla i tame. / Ovo more što se miče, ljulja,
				uranja, izranja... nestaje / i nastaje – život je sam. U biseru, u
				školjki slika / sa izvora sačuvana. I jedna i druga strana, i
				kretanja što / naprijed vuče u pravi naš zavićaj. / naprijed –
				natrag. Natrag – Naprijed. Čovjekov zavičaj u / svemiru.«</p>
			<p>Opsežna antologija Ane Horvat More mora (Naklada Đuretić, Zagreb,
				2014.), u kojoj su sakupljene pjesme o moru od naših latinista do
				suvremenih pjesnika, još je je jedan dokaz da je ova autorica (inače
				i pjesnikinja i prozaistica) vjerna sakupljačica tekstova u našoj
				književnosti koje nastoji objediniti jednom temom. Njezina
				antologija pjesama Subića sakuplja pjesme hrvatskih pjesnika
				posvećene životinjama, a antologija Stablopis panorama je naše
				poezije o drveću. Ana Horvat, romantičarski zanesena prirodom
				(velike su njezine zasluge za očuvanje prava životinja), dokazuje
				svojim radom da se povijest književnosti, s obzirom na »vječne«
				teme, može čitati i unatrag.</p>
			<p>U naših latinista i renesansnih pjesnika more se uglavnom
				tretiralo kao misteriozno, nepoznato mjesto, kao nespoznatljiva viša
				sila što buči, huči, pjeni, prijeti, a koju čovjek, kao izopćenik iz
				njezina praelementa, uzaludno pokušava ukrotiti. More je u naših
				starih pjesnika opasno, valovi hrupe, lađe se dižu i ruše, na pučini
				se sudaraju valovi i vjetrovi, oluje dobivaju gotovo kozmičke
				razmjere, nerijetko je bijes mora povezan s gnjevom bogova, a
				katkada se doziva i kršćanski Bog upomoć da umiri mornarima pute.
				Petar Hektorović u moru vidi strahotnu nepredvidivost, pa pjeva: »S
				oštrinom grbini uzmutiše more, / pak bura zapini, dvignuv se sa
				gore. / Vode ne umihu komu posluh dati, / istom se počnihu put neba
				penjati«.</p>
			<p>Bliži dodir čovjeka i vode opjevat će Stanko Vraz: »Mornar kune
				iste sunčne luče, / Kad na moru brod svoj’ vidi stati; / A kad počme
				vjetar ga njihati, / Opoji se, zapjeva, zahuče«. Umjesto prijašnjih
				brzih jedara i vesala, natezanja konopa u epskim borbama sa
				samoživom olujom, Vraz daje malo mirniju pustolovnu sliku mora, iako
				i dalje zvižde vihori i huče bure, no morem se ipak dade proći, ono
				ne skriva u sebi više srdžbe bogova, nego samo mijene prirode na
				koje mornar može odgovoriti mirnim slutnjama.</p>
			<p>Čovjek i more sprijateljuju se u Petra Preradovića: »Plovi, plovi,
				moja lađo, / U koj’ godijer kraj; / Ja ti cilja još ne nađoh, / Sama
				cilj si daj!« More više nije transcedentna sila, nego je rasprostrt
				prostor plovidbe i čovjek sada pokazuje i moć da ovlada njime. Lađa
				više nije klimavi teret u burnome moru, ona je posrednik između
				mornara i sinje vode, a čovjek se u toj vodi održava jer se ufa da
				će njezino raspoloženje pratiti mornarevo.</p>
			<p>Da je more moguće pridobiti ako mu čovjek sasvim preda dušu i
				vjeru pokazuje Hugo Badalić: »Tiha večer ponad mora blaga krila
				širi, / A od mora svježi vjetrić polagano piri, / Pučina se kano
				staklo preda mnome stere, / Ajde amo, dobro momče, mladi
				gondoljere«. Misija je ljudskog bića, dakle, pobijediti strah od
				mora, izoštriti vjeru u njegovu pitomost, izručiti mu se do kraja i
				umjesto neizvjesne plovidbe, ploviti u druželjubivoj igri s njime.</p>
			<p>Još dobrohotniji doživljaj mora opjevat će Silvije Strahimir
				Kranjčević: »Tiho leži, tiho, more moje drago, / Ko sanjarska duša
				djevojčice rujne, / A ni ćuha nema da ga ljubne blago, / Da laganim
				daškom preko njega strujne... / Ne znam što je ovdje tako slatko
				meni / Ozarena oka gledat niz pučinu, / Srebrnu i žarku u sutonskoj
				sjeni, / Ronit joj u boje, pjege i dubinu...« Pučina se javlja kao
				idilično prostranstvo, kao mjesto spokoja, lirske samoće, kao
				duhovna izolacija od svih bremena na kopnu. Pučina, na kojoj se
				čovjek stapa s prostorom bez međa, blješti površinskom ljepotom i
				mistikom dubina; ona je prije svega dar oku, mjesto motrenja i
				dubokog meditativnog mira. Riječ je o lirskoj odi širini što graniči
				s beskonačnim, širini u kojoj biće preispituje granice vlastitog
				bića.</p>
			<p>Dragutin Domjanić katkada će u stihovima izraziti bajkoviti
				doživljaj mora, obilježiti ga kao mjesto tajanstvenosti koje
				razbuđuje ljudsku maštu: »Modrih dubina stakleni dvori / Dveri
				rastvoriše – odaje sjajne, / Koraljne usne cvjetaju tamo, / Oči te
				pozivlju hladne i tajne«.</p>
			<p>Bezbrojni su lirski opisi mora, preplitanje lelujavih dojmova i
				morskih mijena, umrežavanje misli i emocija kojima se pokušava
				prodrijeti u taj zadivljujući iskon. More je i metafora ljudske
				nutrine – sve nijanse njegova raspoloženja slične su nijansama
				ljudske duhovnosti koja se također preoblikuje ovisno o vanjskim
				trzajima, kao što more mijenjaju povjetarci, bure ili kiše. Vladimir
				Nazor tako pjeva: »Jedno noćno more, koje ne zna za san, / Muči se i
				stenje na dnu moje biti. / Uvijek, i dan kad me raji sunčan, jasan,
				/ Čujem kako plače neke crne liti. / Uvijek, i kad radost začas mi
				se javi, / Čujem kako vodu olovnu mi liva / U dubljine duše, pa ih
				skrite plavi, / I sve jače šumi, i sve teže biva«.</p>
			<p>Veoma lirične, sanjarske slike mora koje izaziva blage titraje
				duše dat će Tin Ujević: »Digao se miris od vala / kud god je vjetar
				dunuo, / i prigušen ćuh sa žala / sanjar je osluhnuo./ Govori vjetar
				u more / i čuju ga delfini, / čuju ga preslatke gore / i plavet u
				pozadini«. Ali more nije samo kolijevka za zibanje slika i
				impresija, more je Ujeviću i misterij, gotovo svetost na koju čovjek
				može odgovoriti samo nijemim strahopoštovanjem: »More godi dušama
				gordima, / divljim akordima. / Ono grmi i tutnji, / a svijet ga
				sluša / u pobožnoj šutnji / i bojazni duša«.</p>
			<p>Religiozni doživljaj mora, njegovu božansku oduhovljenost, dat će
				npr. Ante Cettineo: »Ovdje sam, o Gospode, bio odavno prije svoga
				rođenja / u zlatnom kljunu modrokosa na najvišoj pećini. / Ovdje ću
				i poslije svoje smrti ostati u jednoj istini: / u obnavljanju tvog
				vječnog cvjetanja i zrenja«. Vječnost vode dokaz je i božje
				vječnosti – to je još jedan od ontoloških pristupa moru kojem se
				subjekt može samo diviti, jer ne može na nj bilo kavom voljom
				utjecati. No, more može biti i krajnje dobrohotno, zaštitnički
				raspoloženo prema svemu što se u njemu nađe, kao u Dobriše Cesarića:
				»Vedri se nebo. Sunce se rađa. / Plovi iz luke jedna lađa. / Jedna
				što dugo stajaše na doku, /Sva izbijena, s ranama na boku. / More,
				ko mati, vuče je na krilo. / Ljulja se, šapće: ništa nije bilo«.</p>
			<p>Dvostruka je narav morskoga prostranstva – ona je i užas
				(zagubljenje u prostoru), ali i ljepota (potpuno predavanje
				prostoru), no širina mora ne mora uvijek voditi spoznaji ljepote
				beskraja, ona može taložiti melankoliju te se na koncu
				poistovijetiti s ništavilom. Frano Alfirević tako pjeva o moru:
				»Valove sanjam, kraj kojih zaboravih godine, varljive žene, / dubina
				sunca u krvi, što zanosi teže od vina, / čistoću nebesa, gdje su sve
				čežnje utopljene, / prazninu što opaja nad plavetnilom strašnih
				tišina«. More može asocirati i na smrt – zov morskih dubina može
				biti zov odlaska, nestajanja, kao u Jure Kaštelana: »Začarao sam
				more jednim pogledom i svuda me prati. / Jedno daleko tiho more ja
				sam vidio u snu / i ono me zove u svoje flore i faune, u svoje
				dubine, / u svoje predjele gdje je smrt blaga, dobra sestra«.</p>
			<p>Simultanizam morskih mijena i mijena duše vrlo dojmljivo daje
				Vesna Parun: »Kad se more pomiče s kraja na kraj obzorja – to se / s
				njime pomičemo i mi, moja dušo, pokrita sivilom uspomena. / Kad
				more, zaogrnuto bijesom, ustaje iz šutljivih / grobnica predaka – to
				ustajemo s njime nevoljko i mi, / moja dušo, ledino u koju je
				zasijano zrno budućnosti. / More možda nestaje, ali ne umire. To
				valjda nestaje / i duša, ne znajući za čarobnu smrt koju je tek /
				okrznula okrajkom krila!« Vesna Parun piše jedne od najsuptilnijih
				stihova o moru koje zaranja u našu dušu kao što naša duša uranja u
				njega – u uzajamnom preispitivanju duše i mora ima samo taloženja
				bezbrojnih stanja bez konačna odgovora.</p>
			<p>More je jedna od omiljenih tema haiku pjesnika što trostisima
				pokušavaju oslikati njegov mir, modrinu, dodir s nebom, let ptice.
				Tako Vladimir Devide zapisuje: »Modro nebo gore / odijeljeno
				bjelinom galeba / od modrog mora dolje«. Dubravko Ivančan piše:
				»More... / Sa horizonta / silaze valovi«. Slavica Čilaš zapaža:
				»Valovi... / raznose mjesečinu / po uvali«.</p>
			<p>Izrazito misaon, filozofski pristup moru pokazuje Slavko Mihalić
				koji pokušava proniknuti u bit nedjeljivog prostora: »Posadio sam
				sebe na obalu mora, / nanio daleko gdje je sve u srebrnom pjenušanju
				/ i ništa se ne dijeli, ni prostor ni vrijeme«. I Miroslav S. Mađer
				mentalno preispituje more koje zbunjuje krhkim dodirima kraja i
				beskraja što se zrcale na horizontu: »Površina mora je daleka duboka
				tišina / Površina mora je najtiša visina /Ima nestajanje kraja u
				koji se sve prepušta«. Filozofijsko-duhovno doživljavanje mora
				vidljivo je i u poeziji Vesne Krmpotić – more je panteistička vizija
