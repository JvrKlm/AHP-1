			<p>Antologija Ane Horvat odabrani su cvjetovi riječi (anthos+logos!)
				obuhvata 662 stranice u monumentalnom vremenskom trajanju u kojem je
				sastavljačica ili antologičarka obuhvatila gotovo cijelu hrvatsku
				književnost, počevši od godine 1434, kada djeluje Ivan Česmički, pa
				do najmlađe pjesnikinje Marije Certa.</p>

			<p>Samo primjera radi istaknut ću pjesničko ime koje znamo pod
				imenom: Tadija i čudesnu pjesmu Veličanstvo mora, koja je i do danas
				sačuvala svježinu i Tadijin ironijski način komuniciranja: „Sedam
				dječaka u košuljama zelenim, / Prestaše da osluškuju šumorenje mora
				/ Da bi čuli glas učiteljice koja zna sve / O moru: i kako se
				talasa, i kako se pjeni / A nije u nedoumici, kao ja koji sjedim /
				na bliskoj klupi u zavjetarju i ne znam, / Premda su mi usta
				otvorena, kazati / Ni riječi pred veličanstvom mora, / Pred modrim
				šumorom vječnosti.“</p>

			<p>Doista, pred antologičarkom Anom Horvat bio je zahtjevan zadatak
				pronaći nadahnute pjesme o moru, otevši ih fenomenu prolaznosti u
				lijepom i poštovanja dostojnu slijedu godina (1434–1982). Svojevrsna
				hrestomatija mogla bi se koristiti i u obrazovnim, edukativnim
				centrima, posebno danas kad smo okruženi globalnim i konzumentskim
				tendencijama koje nam nameću profitersko koristoljublje, zatvarajući
				i oči i uši pred sve jačim naletom tuđica, posebno anglizama kao što
				su šop, šoping i lajkati. (Posljednju „izmišljotinu“, lajkati, koja
				je gotovo nasilno zamijenila našu lijepu riječ svidjeti se, trebalo
				bi spaliti na lomači zaborava). Mlade bi generacije, a i odrasli,
				mogli obogatiti svoj rječnik i izraz čitanjem stihova u antologiji
				More mora. Ta sintagma zvuči simbolički otkrivajući svu dubinu i
				raskoš odabranih pjesama, gotovo nemjerljivu univerzalnost.
				Osebujna, tjeskobna pjesma Nikole Milićevića Gorko more upravo je na
				tom tragu: „Kada gorčina prilegne odasvud / široka gorka voda, / šta
				da poduzme čovjek, / šta da poduzme čovjek / kud da pliva / gorak /
				u gorkome moru?“</p>

			<p>Vrsni pjesnik (kojeg već pomalo zaboravljaju!), začudni Jure
				Kaštelan, zapisao je: „Lako je moru biti more. I nebu blistati. I
				livadi cvasti. / Ti si nošen morem nosio more ljubavi“ (Konjic bez
				konjika). Tako poetski i ljudski raste i ova antologija donoseći nam
				tugu i tjeskobu prolaznosti, ali i radost života i užitak stvaranja.
				Posebnost ove stihozbirke zanimljiv je podatak da su se u njoj
				ravnopravno našli koncizni, lapidarni haiku-stihovi koji s
				esencijalna dva, tri stiha kazuju sve. Duhoviti Pajo Kanižaj to
				potvrđuje: „Kamen po kamen / bacali po plićaku, / Ukrali more!“
				(Krađa), „Zadnji se redak / Utopio u moru“ (Morski haiku). Pjesnik,
				književni kritičar i esejist Zvonimir Mrkonjić napisao je za More
				mora studiozan pogovor More u čistopisu.</p>

