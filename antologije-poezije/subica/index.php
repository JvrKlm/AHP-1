      <h3>Subića<br><small>hrvatski pjesnici o životinjama</small></h3>

      <p>Subića, hrvatski pjesnici o životinjama<br /><br />U izdanju VBZ Zagreb, u biblioteci Pjesništvo, izišla je antologija «Subića», koju je sastavila Ana Horvat, književnica i zaštitarka životinja, osnivačica i predsjednica udruge «Druga prilika»
      <br /><br />
      Riječ je o antologiji hrvatske poezije nadahnute životinjama napisane od 13./15. stoljeća do danas. Antologija prati hrvatsko pjesništvo od animalisitčkih pjesama napoznatog glagoljaša i Tužaljke protiv ubojice psa Mora Marka Marulića, preko Balada o zaklanim ovcama Dragutina Tadijanovića, do današnjih dana. Među zastupljenim autorima kao što su Petar Zoranić, Fran Krsto Frankopan, Silvije Strahimir Kranjčević, Vladimir Nazor, Miroslav Krleža, Antun Branko Šimić, Dobriša Cesarić, Nikola Šop, Ivan Goran Kovačić, Vesna Parun, Boro Pavlović, Višnja Stahuljak, Zlatko Tomičić, Zvonimir Balog, Petar Gudelj, Danijel Dragojević, Alojz Majetić, Branislav Glumac, Dubravko Horvatić, Luko Paljetak i Ana Horvat, zastupljen je i Tomislav Marijan Bilosnić, književnik koji živi u Zadru.
      <br /><br />
      Predgovor antologiji napisao je Branislav Glumac, a pogovor Alojz Majetić.
      Prva hrvatska animalistička antologija poezije tiskana je uz potporu Ministarstva kulture RH i Društva hrvatskih književnika. Želja nakladnika i antologičarke je da čitateljima i ljubiteljima životinja i onima koji samo poštuju životinjsko pravo na život bez patnje i ubijanja, dobiju uvid u bogato hrvatsko animalističko pjesništva. Jer, kao što je mudro ustvrdio Ghandi, civilizacijski napredak jednog naroda najbrže i najbolje se može prepoznati kroz odnos tog naroda prema – životinjama. Barem što se pjesnika tiče – Hrvatska je, doista, civilizirana zemlja! U antologiji je na 440 stranica, s ukupno 543 pjesme, predstavljeno 165 hrvatskih pjesnika koji su s ljubavlju, divljenjem i suosjećanjem pisali o životinjama.</p>

