        <h5>Misterij prirode</h5>
        <p>
          Zar bi pjesnici, pa i svjetski klasici (sjetimo se samo Baudelaireove pjesme Mačka u Cvjetovima zla) skladali stihove o životinjama da se ne dodiruju duše ljudi i tih sustvorenja u zagonetnom svijetu i življenju? I hrvatska književnost puna je duhovne zoologije, koja životinje uzvisuje kao misterij prirode, koja ih vidi kao božanski prinos vizualnoj i duhovnoj raznolikosti svijeta. Ana Horvat uspjela je sakupiti pjesme stotine hrvatskih pisaca, od Marulića i Vetranovića do naših suvremenika, koje pokazuju suptilni pjesnički osjećaj za božansko jedinstvo životinja i ljudi u prostoru, vremenu i krajoliku. Životinje su itekako upisane u povijest hrvatske književnosti, ali bijahu raštrkane u stihovima sve dok ih Ana Horvat nije pomno objedinila u hrvatski pjesnički zoo. Uvrštena su doista vrhunska pjesnička ostvarenja (Silvija Strahimira Kranjčevića, Vesne Parun, Vesne Krmpotić, Slavka Mihalića, Ivana Slamniga, Josipa Severa, Zvonimira Baloga, Milivoja Slavičeka, Dalibora Cvitana i brojnih drugih autora) koja prikazuju ljude u životinjama i životinje u ljudima, preplećući tako sudbe i egzistencije ljudskih i neljudskih bića, ravnopravnih po duši i kozmičkom usudu. Pjesme su, generalno kazano, ili tužbalice za napuštenim ili ubijenim životinjama (već je Marulić napisao Tužbalicu protiv ubojice psa Mora, uzvisio patnju: „S najvećom vjernošću, Moro, na našu si pazio kuću / Budnije nego što zmaj čuva Hesperida vrt.“), ili su pjesme balade posvećene osami životinje uzbibane u svoj krajolik ili su radosnice što slave nijemu, suptilnu komunikaciju ljudi, životinja i prirode. Najvećim dijelom pjesme su metaforične, slikovite, pune pjesničkih figura što životinji pridaju mističnu ljepotu i slave njezin sklad s pejzažom, pa su tako česte slike životinjskih očiju sa „sanjalačkom žudnjom“, slike krila kao „plamena“, sunca što obasjava pticu „grimiznim žarom“, životinje koja šuti „u carstvu tišine i visine“... Dakako, jer pjesme o životinjama nužno su vezane uz dušu okoline, uz ideju (utopijskog) kozmopolitizma, pa potiču na duboku sentimentalnost, zanos, romantičnost što na vidjelo iznosi ono zajedničko, zagonetno i skrovito.<br />
          Tužbalice pozivaju na humani pogled oko nas, prozivaju ljudsku sebičnost, prikazuju subiće kao nevinu žrtvu – upravo duboka emocija pjesnika budi suosjećanje sa subićima, možda više od zakona, proglasa i parola. U pjesmi Smrt Višnja Stahuljak pjeva o psu na samrti: „On sam čeka da postane mrtva stvar/ i utone kao odraz svoga lika u čelo neba / gdje u mislima mrtvih pasa / zveči neka kost, sasvim plava.“ Slavna Slamnigova pjesma Ubili su ga ciglama završava tragičnom slikom ubojstva: „ubili su ga, ciglama: crvenim ciglama, / crvenu, mrlju su, prekrili, priglama, / iz svega se, izvuko, samo, repić: / otpuzo, pa se: uvuko, u zid, / u zid, uzi, duzi.“ (Nizanje zareza, odnosno prekidanje sintakse, kao da predstavlja jecanje za psom.). Dunja Detoni Dujmić pseću bol prikazat će kao kozmičku: „Gledali su za vama / Kako se njišete cestom, / Gledali su s horizonta / Dok nije eksplodirao (...) Tada su podignuli repove, / Uzašli na nebo, / I opet su vas gledali, / Poštenim očima, / Jer znali su / Da sve bi bilo besmisleno / Kad se i oni ne bi javili / I nebom pronijeli svoju tlapnju.“ Posebno je tužbalicama za napuštenim carevima ulica, mačkama i psima okrenuta Ana Horvat, koja je posvetila život zaštiti napuštenih životinja.
