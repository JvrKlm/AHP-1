        <h5>Mračna povijest sugrađana</h5>
        <p>
          Da bismo razumjeli važan kulturološki pothvat Ane Horvat, koja je u antologiji Subića obuhvatila mnoštvo pjesama hrvatskih pjesnika posvećenih životinjama, koja je, dakle, dokazala da su subića upisana i u književnost kao duše što ih spoznaje ljudska duša, moramo nakratko prošetati kroz mračnu povijest sugrađana sa šapama, krilima i kljunovima. Vjekovima su ta sustvorenja služila ljudskoj praksi i svijesti, prinosila se na oltare kao žrtve, služila kao talismani (zubi, kandže, repovi), kao hrana, čak i kao kulturna potreba (stoljećima su se knjige pisale na pergamentima od janjeće kože, perom!), a rijetko su bile uzvisivana kao božanstva (krava u Indijaca, mačka u Egipćana...) Tek su pustinjaci i sveci (a valja istaknuti Svetog Franju Asiškog kao ljubitelja životinja) bili druželjubivi prema tim Božjim bićima, izjednačavali u srcu sve živo i stvoreno. I srednjovjekovna mistika zadire u duhovno jedinstvo bića, a irski svetac Kevin, zaštitnik životinja, prestaje graditi samostan da ne bi smetao zvijerima, a dok moli, drži gnijezdo kosa, dok mu vidra donosi molitvenik što mu je pao u more. I Sv. Pavlu gavran daje kruh, a dva lava plaču mu nad grobom. No unatoč slikama posvećenja, odnosno oljuđivanja životinja, tijekom čitave povijesti filozofi, kulturolozi, sociolozi uglavnom su bili zoofobični (kao i kršćanstvo koje neljudska bića nije priznavalo kao moralne subjekte), izuzev rijetkih poput Aristotela i Tome Akvinskog, koji i čovjeku i životinji priznaju osjetilnu dušu, Voltairea, koji poništava tezu o životinji kao stroju, Hugoa, koji reče: „U odnosima ljudi i životinja, i cvijeća i svih predmeta Stvaranja, postoji jedna velika etika koja je danas nezapažena“ ili Schopenhauera koji je tvrdio da je „sućut bez granica prema svim živim bićima najčvršće i najsigurnije jamstvo moralnosti“.
          Društvo je, zaključuje Visković, odvajkada bilo zaokupljeno odnosima među ljudima (rasizmom, političkom i vjerskom nesnošljivošću...), odnosno dosadašnja je etika bila uglavnom „antropocentrička i usko humanistička“. No današnje vrijeme civiliziranost vidi i u suodnosu čovjeka s drugim bićima, u priznanju prava svima, što oživljuje i izvorni biblijski mit o Stvoritelju i svemu što on stvori.
