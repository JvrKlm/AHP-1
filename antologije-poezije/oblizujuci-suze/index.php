      <h4>
        Vijenac 572, 4. veljače 2016.<br />
        Književnost<br />
        Hrvatska poezija: Ana Horvat,<br />
        Oblizujući suze: hrvatski pjesnici o ljubavi<br />
        Nevina antologija o ljubavi<br />
        Sead Begović
      </h4>
      <p>
        Ana Horvat nije imala prosuditeljskih ambicija. Ona naprosto zna što je dobra pojedinačna pjesma u dijakronijskom poretku.
        Slijedom toga proizlazi da su dosad antologijski valorizirani pjesnici u nas ujedno i dobri pjesnici o ljubavi – kao što se pokazalo, primjerice, da su „dobri“ pjesnici ujedno dobri i kao haiku-pjesnici, ako tu poeziju pišu
      </p>

      <p>
        Nakon što je na internetu u razdoblju od 2008. do 2012. uredila prvi portal – antologiju izabranih ljubavnih pjesama sa 187 hrvatskih pjesnika od 19. do 21. stoljeća (www.pjesnici-ane-horvat.net),
        Ana Horvat (pseudonim Jasne Palčec) hrabro se odlučila oknjižiti hrvatsku ljubavnu liriku u pozamašnoj i lijepo opremljenoj knjizi pod naslovom Oblizujući suze: hrvatski pjesnici o ljubavi.
        Riječ je o preglednom zborniku ili panorami (autorica se ne postavlja u arbitrarnu poziciju antologičarke) s velikim indeksom imena (kronološki poredanih s obzirom na književnu povijest i godinu rođenja) od iliraca i
        preporodnih pjesnika Stanka Vraza, Ivana Mažuranića, Augusta Šenoe, Augusta Harambašića, pa od preteča modernih previranja S. S. Kranjčevića i već amblematičnih pjesnika moderne A. G. Matoša i Vladimira Vidrića do pjesnika koji su svojom snagom označili prijelaz iz 19. u 20. stoljeće Vladimira Nazora i Milana Begovića.
        Slijede nezaobilazni pjesnici koji su ljubavnim damarima obilježili našu suvremenost, svaki na svoj način: Kamov, Ujević, Andrić, Krleža, Šop, Tadijanović, I. G. Kovačić, Dizdar, Kaštelan, Boro Pavlović, Mihalić, Slaviček, M. S. Mađer, Ljerka Car Matutinović, Arsen Dedić, Zidić,  Stamać, Petrak ...
      </p>

      <h4>Prešućen princip odabira</h4>
      <p>
        Mnogi zastupljeni pjesnici nisu više među nama, ali je i golemi broj živućih i još ljubavno aktivnih pjesnika i pjesnikinja spremnih da smisao svojih ljubavnih ritualnih egzistencija najvruće opjevaju.
        Napokon najveći je broj suvremenih pjesnika koji su autorici bili dostupni kada je na tribinama u zagrebačkim knjižnicama i Klubu Studentskog centra predstavila više od pedeset hrvatskih pjesnika i pjesnikinja.
        Autorica je, vjerojatno ohrabrena uvrštenjem svoje ljubavne lirike u internetsku Svjetsku antologiju poezije (uz 2500 pjesnika iz 156 zemalja) uvrstila u ovu knjigu i sedam svojih pjesama, što antologičari obično ne rade,
        ali ovdje će to biti shvatljivo jer time sastavljačica potvrđuje da nije imala antologijsko-prosuditeljskih ambicija. Ona naprosto zna što je dobra pojedinačna pjesma u dijakronijskom poretku. Slijedom toga proizlazi da su dosad antologijski valorizirani pjesnici u nas ujedno i
        dobri pjesnici o ljubavi – kao što se pokazalo, primjerice, da su „dobri“ pjesnici ujedno dobri i kao haiku-pjesnici, ako tu poeziju pišu. No autorica pokazuje i veliku otkrivalačku hrabrost uvrstivši neke pjesnike koji su slabo odjekivali u književnoj javnosti
        (Jadranka Čolović Sviličić, Mladen Medar, Silvija Butković, Damir Žubčić, Darko Beljan...), a možda su to zaslužili pa makar i kao pjesnici o ljubavi. Pokazat će se još i to da svi pjesnici nemaju isti ljubavni žar i istu ljubavnu fiziologiju iako je u konačnici,
        u raspletu ljubavne dramaturgije, posve jasno da je ljubav nešto više od posjedovanja, razumijevanja i uporabe neke stvari te da i njezina smrt nije samo kraj – točka iza života.<br />
        <br />
        Toliko je poznatih imena da možda stoga ne nailazimo na biografije i biobibliografije uvrštenika, a nemamo ni predgovorni ni pogovorni kredo iz kojeg bismo ponešto saznali o principima periodizacije, autoričine metode što se tiče izbora i slično.
        Umjesto toga nailazimo na simpatično proizvoljan proscenij ove knjige, koji se sastoji od predgovora, kratkoga teksta o poetskoj estetizaciji i funkciji ljubavi Zvonimira Goloba i isto tako zgusnuti mudroslovni amalgam o ljubavi filozofa Milana Kangrge:
        „Čovjek je morao postati čovjekom uopće, da bi mogao ljubiti kao biti (stalno iznova postojati). Smrt ljubavi, odnosno njezino iščezavanje iz samih temelja čovjekova svijeta... bila bi ne samo smrt istinskog i najdubljeg izvora poezije ljudskog života, nego i najsigurniji pokazatelj da je čovjek ugrožen – da nestaje s lica zemlje.“
        Kangrga dakle analizira ljubav kao „osnovni poriv i traganje za smislom života te njegovo neprekidno utjelovljenje“.
      </p>

      <h4>Dobra informiranost</h4>
      <p>
        Nama preostaje da na tom tragu iščitamo nekoliko uvodnih ljubavnih pjesama koje je autorica zamislila kao malu predantologiju za koju nismo sigurni što zaista znači – možda pjesmovno radni predložak za ljubavnu poeziju koja slijedi?
        No disparatni poetski primjerci koji sinkrono slijede pomno su odabrani biseri Ane Horvat, Vladimira Kovačića, Petra Preradovića, A. B. Šimića, Dobriše Cesarića, Drage Ivaniševića, Vesne Parun, Ivana Slamniga, Zvonimira Goloba, Nade Iveljić, Vjekoslava Majera, Vesne Krmpotić, Josipa Pupačića, Irene Vrkljan,
        Danijela Dragojevića, Andriane Škunca, Jakše Fiamenga, Enesa Kiševića, Jozefine Dautbegović, Luke Paljetka, Jagode Zamoda, Sime Mraovića, Ane Brnardić, Tita Bilopavlovića i Dorte Jagić.<br />
        <br />
        Svi su ovi pjesnici i naknadno uvršteni dok sad „zjape“ kao radni primjerci. Neke su pjesme posebno indikativne. Spomenimo dva humorna katrena pod naslovom Pjesma Ivana Slamniga:<br />
        <br />
        Ti si nemoguća babuskara<br />
        al za to mi milo puca<br />
        jer srce nema očiju<br />
        srce nema ni srca<br />
        pa ne pita da li se laže<br />
        ne pita za kakvoću.<br />
        Sve su mi druge draže<br />
        a samo tebe hoću<br />
        <br />
        Dakle pred nama je odjednom lapidarni stih, ispunjen buntom, ironijom, infantilnim, ali originalnim poimanjem ljubavi koja nalikuje igri, a zapravo je odraz rijetkoga ljubavnog doživljaja koji se naposljetku pokazuje kao sreća i ispunjenje.
        Pjesma nam govori da čovjek ima svoje fizičke i psihičke „migove“ te možemo govoriti o taktilnoj ljubavi i ljubavi koja se manifestira kao psihoseksualnost. I jedno i drugo bit će poklič životnog zanosa sama postojanja kao razdragani glasovi fazana koji se udvaraju ili pare u polju.
        Ali ljubav nije Eden. Ona je kao i obitelj – jamstvo za raj ili za pakao. Pjesnik koji literarizira, ili se igra jezikom, koji čita druge da bi pisao o ljubavi – nema se čega bojati, no pjesnik koji istinski proživljava vlastitu dramu ljubavi, pogotovo ako ga u njoj opeče predraga meduza, zabugarit će u vlastitom kipućem kotlu žarke ljubavi.<br />
        <br />
        U ovoj je knjizi zastupljen i velik broj mladih suvremenih pjesnika (ne i najmlađih), poneki već i pjesnici srednje generacije – dokle i doseže znalačka interesna sfera i znalački uvid sastavljačice zbornika, naime, kvalitativnu bi formaciju u ovoj knjizi bilo teško normirati budući se ljubavno, kao i lijepo, definira uvijek zasnovano na subjektu.
        Harmoniju i cjelovitost bića, sviđanja po sebi, apsolutnu sintezu međusobnoga prožimanja, ograničenu i beskonačnu slobodu (posebice u primislima onih autora koje neodoljivo privlači situacija kada jedno tijelo pokrije drugo, dakle, seksualna situacija), idolatriju i odioznost, moguće je otkriti, ali ih je nemoguće vrednovati s obzirom na oporbeni dualitet poetski lijepa i poetski ružna, a ponajviše zbog razlikovnoga spora oko i između erotike i seksualnosti.<br />
        <br />
        Nadalje, ljubav više nije u ropstvu. Kultura i civilizacija današnjice došaptavaju medijatorima ljubavi posve nove zadaće i stečena iskustva na relaciji muškost – ženskost.
        Pa što onda očekivati od ljubavi – ponovno uspostavljanje harmonije na ruševnim temeljima stare ljubavi? I tu nam, u ozdravljenju, pokušavaju pripomoći pjesnici i pjesnikinje, bez i jednog pucnja, osim u voljenog, a koji su se razmiljeli svijetom kao ljekovita trava koju, osim što je na nekim mjestima kose – malo njih još i pase ili dolazi na njezinu ispašu.
        No pjesnici nisu skloni samosvladavanju i dok su nekoć, u Harambašićevo i Kranjčevićevo doba žalobno bugarili (kao da pjesme pišu u histeriji, rekao bi A. G. Matoš), u naše se doba slobodno oslobađaju viših oblika vlastitoga „ja“ te se u njihovim poetskim mislima slobodno odražava stvarnost sveukupnoga ljudskog usuda – sve do pikantnih potankosti.
        Rekli bismo – nema više skrivanja i prikrivanja, no može li se o svemu i pjevati?<br />
        <br />
        Ne može, također bismo rekli, iako nema više posve nevinih antologija ljubavne poezije, budući da u ljubavi ima i stradanja. Koliko u ovom „izboru“ zapažamo odsutnost čak i liberalnog doziranja seksualnosti, pokazat će vrijeme i njegovi novi kriteriji, napuci i nalozi.
        Nadamo se da uvjeti za to neće biti unos bizarnog i skarednog, dok će neki upravo to očekivati. Iako se i toga u nekim medijima mnogi više ne gnušaju, pa što se toga tiče možemo slobodno zaključiti da nema više anđela među nama koji pobuđuju ljubav na jastučićima oblaka.
        Ljubav može biti i socijalni problem, ako vas zatvore u podrum bez prozora i nabiju vam režije i ovrhe. Ima li takvih ljubavnih okolnosti u ovoj knjizi? I želi li ljubav iznovice uspostaviti sklad svake ličnosti kojoj prijeti razaranje? A ljubiti se mora!<br />
        <br />
        One koji smatraju da postoje dva načina života – religiozni i seksualni – pitamo: za koji će se oblik odlučiti? Za prvi koji vodi u prosvjetljenje i spasenje opredijelit će se već spominjani anđeoski tipovi, a za drugi oni koji od ljubavi očekuju „golu“ ugodu i nikad utaženu žudnju.
        No put ljubavi je nedvojbeno i religiozni doživljaj, pa sad, je li to predmet (i može li biti) predmet ove (uvjetno rečeno) antologije ili on seli u antologiju duhovne ili sakrosanktne poezije? Možda ipak ne treba širiti temate i motive (jer ćemo uskoro imati knjigu ­Hrvatski pjesnici o ambroziji i sl.) pa makar oni bili i duhovnoga karaktera.
        Ako poezija ide uz vrijeme, onda i duhovno stoji usuprot potrošačkom, koje nam se zavodljivo smiješi, stoga, što će nam civilizacija ljubavi koja nudi samooslobođenje? Imamo li takvih mrgodnih pjesama o ljubavi u ovoj knjizi i trebaju li nam ­uopće?<br />
        </p>

        <h4>Izostanak provokativnih pjesama</h4>
        <p>
          Napokon, predmet ljubavne poezije može biti i ono besramno. Spomenimo samo ono homoerotsko, ali to je još (ako nije tabu) od strane oficijelne književne kritike sankcionirano, a one underground (uz sodomiju, edipovštinu, incest, bolesnu fetišizaciju itd.) honorirano.
          Proizlazi, barem što se tiče dostupnosti pornografije, ono što je danas besramno, sutra više nije. Ali ima i u tome neke uzbudljive ljepote. Spomenimo mapu pjesnika Milana Begovića i slikara Milivoja Uzelca pod naslovom Grčka proljeća.
          Iako Begovića autorica uvrštava s već toliko puta uvrštenim pjesmama Ponoć i Liddy, ne možemo se oteti dojmu da je ovaj veliki pisac uskraćen s neuvrštenim pjesmama upravo iz te mape ilustrirane Uzelčevim rajc-grafikama.
          Riječ je o sonetima koji su danas vjerojatno postali benigno stihovlje koje je moglo testirati čitateljstvo i odati hrabru sastavljačicu, a onomad je služilo izabranoj gospodi kao mizanscena na raspojasanim (orgijastičkim?) domjencima.
          Ta je samizdatska mapa preskočena, a možda i još nešto u ovoj knjizi pa su svi izgledi da ćemo još pričekati za ulazak takvih djelca (uz to pisana s vrhunskim artizmom) u izbore i antologije. Jer seksualne operacije u poeziji, kao i u životu, mogu probuditi ljubav ili je razoriti pa će sastavljači uvijek biti zdvojni, a mi im ne možemo na tome zamjeriti.<br />
          <br />
          Zato postoje nevino pisane antologije za nevino normalne i one usmene i samizdatske za one drukčije čitače, za sada abnormalne. Treba se samo odlučiti.
          Unaprijed znamo da pjesnici obično ne žele dobiti otkaz od društva ako o ljubavi pišu drukčije – bolje je umiljavati se.
          Uostalom, ljubav nije samo jedna životna faza i osmišljeni eros može i ne mora prethoditi pustoj seksualnosti koja će kratkoročno zadovoljiti našu žudnju i onda je opet replicirati kao glad, kao žeđ.
          Ipak, egoizam pobjeđujemo dramom sjedinjenja u ljubavi. Bilo kako da označimo vječnu ljubavnu igru u prirodi i društvu, ili u kućici u spavaćici – iz nje ne možemo ižmikati ni dan ni noć moćne strasti – ona nas ili obasjava ili zatamnjuje.<br />
          <br />
          Ljubav je svjetska drama i zato je treba širiti u bilo kojem obliku dok posve ne zavlada. Jedan je od načina i plemenita i dobro odrađena zadaća ovog izbora, pa zašto ne reći – za nas i antologije!
        </p>
