                Pjesnički kod i hod Ane Horvat ne odvaja se od semantike tijela, osjećaja i čula i ta bliska povezanost sa svojim ja približava je više jednoj tradicionalnijoj ljubavnoj i refleksivnoj poeziji.(...)Pjesme Ane Horvat prožete su mirisima kuhinje i ženskog tijela i bića - i to onim mirisima kakvima ih žena sama doživljava, a ne kojima je opisuju drugi.(...) To je, dakako, opće mjesto u većini razgovora i prepirki o tzv."ženskom pismu", ali taj pomak u očištu nikako da se dogodi.No, nije dovoljno da se promijena perspektiva dogodi samo u području književne kritike. Ona se mora zbiti i kulturološki, tj. moraju postojati kulturni uvjeti u kojima žena doista ima pravo na vlastiti glas, na svoje ime i svoj doživljaj.(...)
                U svakom slučaju, poezija Ane Horvat i njeno stalno vraćanje na ljubav kao osnovni motiv, bez sumnje popunjava jedno prilično osiromašeno mjesto unutar moderne hrvatske ljubavne lirike, koja, izgleda, sve više zazire od ljubavi. A bez ljubavi - neki još uvijek u to uporno vjeruju - nema ni poezije niti života, a možda i sve to što činimo, radimo (samo) zato da bismo bili voljeni. 
