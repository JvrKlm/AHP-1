			<h3>
				PREDGOVOR<br> <small>Tomislav Maretić</small>
			</h3>

			<p>
				Haiku pjesništvo vezano je ponajviše za prirodu pa je tako, uz
				biljke i drugo česta tematika haiku pjesama u Japanu i izvan njega.
				Međutim i ova tematika se razlikuje od pjesnika do pjesnika, a među
				"velikom četvorkom" japanskih haiku majstora Issa je najviše pjesama
				posvetio životinjama i to svim vrstama, uključujući one najsitnije,
				kao recimo skakavce, puževe, muhe, uši, buhe i drugo, na neki način
				"bez diskriminacije".<br> <br> Ana Horvat, pjesnikinja,
				antologičarka, novinarka, pravnica, balerina, plesačica folklornih
				plesova velika je ljubiteljica prirode što je jasno vidljivo iz
				njezinih dosadašnjih vlastitih zapisa kao i antologija. Moglo bi se
				reći da je ona nastavljačica Issine crte u hrvatskom haiku vezanom
				za životinjski svijet. Nije čudo da su njezine haiku pjsme ušle u
				svjetsku zbirku "Every Chiken, Cow, Fish and Frog: Animal Rights
				Haiku Paperback, autor i izdavač Robert Epstein i Miriam Wald
				(2016.). Njezine pjesme odraz su njezina života kao i aktivizma.
				Autorica je prije svega njihov prijatelj i udomitelj, kao i Issa, no
				ujedno angažirani aktivist osnovavši udrugu za zaštitu životinja
				"Druga prilika" i udomljujući prije svega pse i mačke. Očito je da
				je njezin odnos proizašao iz naprosto ljubavi prema živim bićima i
				osobito onima nezaštićenima koje se koristi i iskorištava u svakom
				zamislivom smislu od strane čovjeka.<br> <br> Nažalost, primjeri su
				posvuda oko nas i ne ograničavaju se samo na animalnu prehranu
				čovjeka kao sveždera, nego i na odnos prema životinjama kao bićima
				bez prava. Premda autorica ne može sakriti svoj revolt protiv takve
				prakse njezine pjesme nisu izraz pukog aktivizma, ponajmanje
				"plaćenog aktivizma", naime autorica se njima druži, možemo slobodno
				reći - prijateljuje, živi i surađuje, a radi u udruzi volonterski. K
				tomu, ona poput Isse u nizu svojih haiku u prijateljestvu i
				promatranju životinja pronalazi i humor.<br> <br> Zbirka Životinjski
				haiku sastoji se od 130 haiku, uglavnom novih uz pokoji iz ranije
				zbirke "Čučerski haiku za Emily“, Naklada Đuretić, 2013. Zbirka nema
				podnaslove niti međusobne ovisnosti susjednih haiku, a u svakoj
				pjesmi se redovito spominje životinja od kukaca, ptica, sisavaca do
				riba, glavonožaca i školjaka. Trostisi su gotovo nepogrješivo u
				slogovlju 5/7/5 kako zahtjeva klasični haiku. Što se tiče oznake
				godišnjeg doba, one su u haiku imanentne to jest vezane za neku
				životinju (recimo mrav je oznaka ljeta, kao i šišmiš i sl) tako da
				su sezone prisutne ako i nisu napomenute. Većina japanskih haiku ima
				"kireji", stanku, koja se u zapadnim jezicima obično označuje kao
				crtica, točka/zarez ili tri točkice. Pjesnikinja ne označuje te
				pauze, no oni se naziru u ritmu pojedinih haiku pa ih se može lako
				zamisliti. No većina je trostiha u jednom dahu, izjavna, povremeno
				refleksivna, s čime će se složiti moderni haikuisti ("gendai haiku")
				za razliku od ovog konzervativnog osvrtaša. Untoč toga to ne mijenja
				pozitivni dojam ovih šarmantnih haiku "poput Isse". Razlika načelna
				jest u tome što se u autoričinim pjesmama osjeti uz ljubav i
				aktivizam, dok je u Isse samo ljubav. Sasvim je jasno da je autorica
				toga svjesna i da je njezin pristup namjeran, baš kao što su to
				manifestirali i drugi pjesnici kako u spomenutoj antologiji Roberta
				Epsteina, tako i u nizu novih gendai-haiku glede drugih sadržaja.
			</p>

