            <p>
                Bez obzira na sve ovovremenske varijacije perturbacija globalističkih najezda i sve dubljeg poniranja u srž života koji se kaotičnorastvara kao da je podvrgnut tzv. magnetskoj rezonanci što nesmiljeno otkriva patološke promjene u ljudskom organizmu i u organizmu svijeta, poezija će usprkos svemu ostati jedinstven izbor postojanja koji će taj skučen i načeti život makar i fiktivno osloboditi od straha i svakovrsnih nedaća, učinivši ga „jestivim“ na razini mogućeg–nemogućeg i u tom kontrapunktu osjećajnog dovesti do svojevrsna ushita životom, što je samo po sebi paradoks.<br>
                <br>
                Izd. Vlastita naklada Đuretić, Zagreb, 2013.<br>
                Nova knjiga Ane Horvat pod osebujnim naslovom Čučerski haiku za Emily, u tolikoj je mjeri individualna da se po njoj prolaznost življenja, taj beskonačni circulus vitiosus čini podnošljivim kao „gnijezdo u draču“, u kojem još ima mjesta za neke transcendentne valere koje promiče poezija, oduvijek otvorena za neka sudbinska pitanja.<br>
                <br>
                Stotinjak haiku-kompozicija (a o haikuu kao pjesničkoj formi napisano je na stotine stranica pa se sad neću time baviti!), iz pritajene tjeskobne napetosti koja prati pisanje i žudnju da se izabranim, esencijalnim riječima ovlada stresom prožeta zbilja, dovodeći se izvornim, imaginativnim razmišljanjem na čistinu promatranja sebe
