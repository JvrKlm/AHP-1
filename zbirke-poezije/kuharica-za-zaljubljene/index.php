                        Šarm knjige neosporno leži, prije svega, u ideji, koncepciji i pristupu. Zamjerno velik broj recepata pomno odabranih za intimne domjenke u dvoje, garnirani su kao kakva poslastica duhovitim stihovima koji se pjene poput najboljeg šmapanjca.(...) </p>
