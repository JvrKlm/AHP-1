                    <p>(...) Govore o jelu, a pričaju o ljubavi na nov, ovoj knjizi prilagođen način, sa savjetima kakve su prije dvije tisuće godina davali rimski pjesnici Propercije i Katul o količini poljubaca potrebnih da bi ljubavna kemija dosegla gotovo gastronomski vrhunac.
                        Ovo je knjiga za intelektualce koji cijene originalnost pristupa, gurmane koji znaju što je dobro jelo, veseljake koji mrze i najbolje stvari u dosadnoj ambalaži, posebno zaljubljenike koji bi, u magli zanosa možda pojeli s užitkom sve, ali...zahvaljujući autoricama ove superzanimljive knjige, jesti će lijepo i još ljepše ljubiti. (...) </p>
