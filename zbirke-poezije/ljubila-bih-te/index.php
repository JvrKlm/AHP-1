			<h3>LADA ŽIGO ŠPANIĆ, o zbirci</h3>
			<h6>
				Ana Horvat, Ljubila bih te<br>Ljubav s krilima na nebu i s nogama na
				zemlji!
			</h6>
			<p>Ana Horvat, pravnica i književnica, osnivačica privatnog
				savjetovališta za žene „Eva“, osnivačica udruge za zaštitu
				napuštenih životinja „Druga prilika“, organizatorica virtualne
				antologije „Hrvatska ljubavna lirika“ (koju financira iz svoje
				mirovine), objavila je do sada petnaest zbirki poezije (od toga
				dvije slikovnice za djecu), roman Podsuknja, zbirku priča Pismopriče
				te važne, nezaobilazne antologie: Subića, Stablopis, More mora i
				Oblizujući suze.</p>
			<p>Izabrane pjesme Ane Hrvat Ljubila bih te obuhvaćaju pjesme iz
				njezinih mnogih zbirki, npr. Kutija za suncokrete, Male pjesme, Neke
				druge riječi, Mjera blizine, Kuharica za zaljubljene, Modri pleter
				itd.</p>
			<p>Kako načelno tretirati ljubavnu poeziju Ane Horvat u kontekstu
				cjelokupne hrvatske ljubavne lirike?</p>
			<p>Prije svega, uglavnom se opjevava ljubav iz ženskoga kuta, odnosno
				progovara ženski glas koji je rijedak u hrvatskoj ljubavnoj lirici
				do najnovijih dana (mnoge pjesnikinje danas pišu o ljubavi u skladu
				sa oslobađanjem ženstva, za što je velikim dijelom zaslužna i
				feministička borba za ženska prava. Tako će Ana Horvat spomenuti u
				svojoj poeziji Ericu Jong i Doris Lessing, jedne od autorica koje su
				utjecale na spisateljice diljem svijeta).</p>
			<p>Kako ljubav može oblikovati, preoblikovati, usrećiti, unesrećiti
				žensko biće? Dok je u tradicionalnoj lirici u prvom planu
				zaljubljeni muški subjekt, a žena je negdje u pozadini (kao vila,
				gospa, ideal…), u ovoj zbirci u prvom je planu žena, dok su muškarci
				(muževi, ljubavnici…) u drugom planu, kao nužna (su)bića i prateće
				sjene što utječu na esenciju ženskoga subjekta.</p>
			<p>Zbirka izabranih pjesama Ljubila bih te također pristupa ljubavi i
				tradicionalno, odnosno romantično i metafizički (ukrašena je
				atributima prirode), no Ana Horvat često ljubavi skreše krila, pa se
				ona stropošta u zbilju, da vidimo kako to izgleda i u praksi. Dakle,
				za razliku od tradicionalne ljubavne lirike koja je uglavnom duhovna
				(muška) lirika, prepunjena čežnjom za neosvojivom ženom, Ana Horvat
				opjevava i filozofiju i praksu ljubavi! Ljubav nije samo nebo,
				žudnja, nego i zemlja na kojoj valja saditi, prikupljati plodove,
				ali i čupati sav korov što ga nesretna ljubav ostavlja za sobom.</p>
			<p>Ljubav u poeziji Ane Horvat nije samo u duši – ona je i u kuhinji
				gdje žena peče kolače, i na livadi punoj krizantema, i u mjesečini,
				i u postelji, i pored maloga stola sa svjetiljkom i u kozmetičkom
				salonu… Ana Horvat u svojim ispovjednim pjesmama (u kojima se obraća
				Drugom) opjevava ne samo esenciju ljubavi, nego i njezinu
				svakodnevicu, pa su zato pjesme dinamične, živopisne, odnosno imaju
				kretnje, ritam, okus, miris, boju, sliku, tj. veoma su nam zorne i
				bliske.</p>
			<p>Zbirka Ane Horvat daje sve fenomene ljubavi. Pjesme su zaigrane
				poput leptira koji sada slijeće na livadu, sada ulazi u sobu, sada u
				kuhinju, pa opet oblijeće prirodu. Daju se svi aspekti ljubavi -
				poetski, svakodnevni, bračni, vanbračni, metafizički, fizički,
				misaoni, emotivni, veseli, neveseli… A i žena zadobiva mnoštvo uloga
				– vjerna žena, nevjerna žena, žena-pralja, žena-kuharica,
				žena-ljubavnica, sretna i nesretna žena…</p>
			<p>Ana Horvat često uzvisuje ljubav kao počelo (što kazuje i zbirka
				„Počela“) – opisuje kozmičku ljubav praelemenata (vode, zemlje,
				vatre, zraka), kako bi uzdigla prirodu u vječnom ciklusu u kojemu
				nam ona daruje harmoniju. U ovakvim pjesmama osjećamo milijune
				iskonskih poljubaca, preplavljuje nas osjećaj metafizičke ljubavi
				duše i prirode. To su poetsko-filozofske pjesme. Tu spadaju i pjesme
				o stablima iz zbirke „Stablopis“.</p>
			<p>Ana Horvat često opjevava ljubav kao sjedinjenje s drugim – to su
				romantične pjesme o odanosti, vjeri, davanju cijeloga bića Drugom, o
				bezuvjetnoj ljubavi. Te su pjesme baletne, pune verbalnih pirueta,
				stilski elegantne, lirične, čeznutljive.</p>
			<p>No, pojavljuje se u zbirci i ljubav kao nemogućnost sjedinjenja.
				Ljubav je sav onaj neizrečeni međuprostor između dvaju bića, ona je
				uvijek neostvareno savršenstvo. S vremenom nestaje pozlata na
				prstenu ljubavi – ljubav, naime, započinje kao svečanost nade,
				vjere, žudne, ali se s vremenom iluzija urušava, čak i do
				autodestrukcije. Ostaju tuga, suhi herbarij uspomena, zagonetna
				tišina u kojoj bi ona htjela nešto reći, ali on to ne bi znao čuti
				(pjesma „Tišina“). U pjesmi „Staza“ ona romantično utire staze
				kojima on ne može doći, u pjesmi „Proljetna šetnja“ ona gleda dva
				starca, osunčana i osjenjena uspomenama, i tvrdi da ima netko s kim
				je htjela tako izgledati, ali nikada neće. U pjesmi „Ničija djeca“
				pita se kamo odlaze ljubavni pupoljci, imaju li neku kuću gdje
				prezime, pamte ili zaboravljaju one čiji su bili. U pjesmi „Ozračje“
				boji se otvoriti kutiju s pismima koja dišu prašnjavi zrak, da ne
				poremeti spavanje i ravnotežu.</p>
			<p>jesme o rastanku iznimno su misaone, emotivne, mistične, lirične,
				ali neke su i kruto realistične, poput pjesme „Rastanak od susreta“
				– u toj pjesmi pjesnikinja se obraća svim rastancima, nježnim,
				užasnim, galantnim, osvetoljubivim, obraća se muškarcima za koje je
				uzalud kuhala, pa završava: „nećemo se sresti i varati ni
				sastajati“. Stavlja prkosni uskličnik, proglašava rastanak
				činjenicom, kako ne bi upadala u zamke patosa napuštenog bića. U
				Pjesmi „San o kalifornijskoj glisti“ žena se poslije rastanaka vraća
				samoj sebi – ne želi biti neko ostavljeno nedovršeno biće, nego se
				spaja sama sa sobom, opjevava samu sebe, svoju zrelost koja je
				nabujala poput klupka sa svim sačuvanim i pokidanim nitima.</p>
			<p>Mnogo je pjesma koje opisuju ljubav kao rutinu – to su pjesme,
				kako autorica kaže u jednom stihu, „bez čipke i krila“, pjesme
				isjeckane od kuhanja, prašnjave od pospremanja. Jer „ljubav je sol u
				kruhu života“. U tim pjesmama ljubav je trivijalna pojava u našem
				automatiziranom, autističnom vremenu, ona se brzo usvaja, brzo
				troši, brzo odbacuje u scenografiji kojekakvih pomodarija,
				preparata, umjetnih dodataka ljubavi. U pjesmi „Žensko vrijeme
				sadašnje“ pjevat će, otprilike: brzo spavaj, brzo meti brzo kuhaj,
				brzo rađaj, ljubav je vrijeme, novac… U pjesmi „Darovnica“ nabrajat
				će pedikere, kozmetičare, šminkere, depilacije kako bi on zavolio
				njezin njegovani stari leš. U pjesmi „Prigodna prodaja“ reći će:
				„Kupio si me na rasprodaju plaćajući riječima kao čekovima bez
				pokrića“.</p>
			<p>Te su pjesme prozaične, urbane, „stvarnosne“, jer Ana Horvat
				prilagođava stil tipu ljubavi o kojoj pjeva. Iznimno je stilski
				raznovrsna – kada pjeva o enigmi ljubavi, metaforički će zaći u
				labirinte duše, kada pjeva o trivijalnoj ljubavi, odbacit će sve
				klasične urese, pisat će kruto realistički, kada piše o počelima,
				razvit će poetsko-filozofijski ples riječi.</p>
			<p>Često se javlja i ljubav kao sudbinska anegdota - to su kratke
				duhovite pjesme u kojima se ljubav svodi na slikoviti fragment,
				isječak koji u malo riječi kaže sve. U tim kratkim pjesmama Ana
				Horvat začudno i pronicljivo, u tren, rendgenski ulazi u smisao
				ljubavi, u njezinu ukletu sudbu prolaznosti. Pjesme su pisane kao
				gnome – kratke, mudre sentence koje nas često nasmiju do suza.</p>
			<p>U zbirci su i bajkovite ljubavne pjesme o voću i povrću i još
				štošta… Ana Horvat obuhvatila je ljubav u svim manifestacijama,
				opjevala ju je svim stilovima, zašla je u njezin nukleus duboko i
				mudro, ali ju je i prkosno i duhovito odbacila kao iluziju. Tako smo
				dobili iznimno raznovrsnu ljubavnu liriku koja se plodi u duši, ali
				se živi u zbilji.</p>
			<p>Lada Žigo Španić</p>
